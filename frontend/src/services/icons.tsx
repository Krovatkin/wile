export function isImageFile(filename: string): boolean {
  const ext = filename.toLowerCase().split('.').pop();
  return ['jpg', 'jpeg', 'png', 'gif', 'webp'].includes(ext || '');
}

export function isDocumentFile(filename: string): boolean {
  const ext = filename.toLowerCase().split('.').pop();
  return ['docx', 'doc', 'xls', 'xlsx', 'ppt', 'pptx', 'pdf'].includes(ext || '');
}

export function getFileIcon(filename: string, type: 'file' | 'folder'): () => JSX.Element {
  if (type === 'folder') return Icons.folder;
  if (isImageFile(filename)) return Icons.image;
  if (isDocumentFile(filename)) return Icons.document;
  return Icons.file;
}
